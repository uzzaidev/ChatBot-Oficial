import fs from "node:fs/promises";
import path from "node:path";
import puppeteer from "puppeteer";

const BASE_URL = process.env.IOS_SCREENSHOT_BASE_URL ?? "https://uzzapp.uzzai.com.br";
const DEMO_EMAIL = process.env.DEMO_EMAIL;
const DEMO_PASSWORD = process.env.DEMO_PASSWORD;

const PROFILES = {
  ipad_13: {
    outputDir: "docs/ios/screenshots/auth-ipad-13in",
    viewport: {
      width: 1032,
      height: 1376,
      deviceScaleFactor: 2,
      isMobile: true,
      hasTouch: true,
    },
    screenshotPixels: "2064x2752",
    userAgent:
      "Mozilla/5.0 (iPad; CPU OS 17_0 like Mac OS X) AppleWebKit/605.1.15 (KHTML, like Gecko) Version/17.0 Mobile/15E148 Safari/604.1",
  },
  "6_5": {
    outputDir: "docs/ios/screenshots/auth-6.5in",
    viewport: {
      width: 428,
      height: 926,
      deviceScaleFactor: 3,
      isMobile: true,
      hasTouch: true,
    },
    screenshotPixels: "1284x2778",
    userAgent:
      "Mozilla/5.0 (iPhone; CPU iPhone OS 17_0 like Mac OS X) AppleWebKit/605.1.15 (KHTML, like Gecko) Version/17.0 Mobile/15E148 Safari/604.1",
  },
};

const selectedProfileKey = process.env.IOS_SCREENSHOT_PROFILE ?? "ipad_13";
const profile = PROFILES[selectedProfileKey] ?? PROFILES.ipad_13;
const OUTPUT_DIR = path.resolve(profile.outputDir);

const routes = [
  { name: "01-dashboard.png", route: "/dashboard" },
  { name: "02-conversas.png", route: "/dashboard/conversations" },
  { name: "03-contatos.png", route: "/dashboard/contacts" },
  { name: "04-crm.png", route: "/dashboard/crm" },
  { name: "05-configuracoes.png", route: "/dashboard/settings" },
];

async function login(page) {
  await page.goto(`${BASE_URL}/login`, { waitUntil: "networkidle2", timeout: 120000 });
  await page.waitForSelector("#email", { timeout: 30000 });
  await page.waitForSelector("#password", { timeout: 30000 });

  await page.click("#email", { clickCount: 3 });
  await page.type("#email", DEMO_EMAIL, { delay: 15 });
  await page.click("#password", { clickCount: 3 });
  await page.type("#password", DEMO_PASSWORD, { delay: 15 });

  await Promise.all([
    page.click('button[type="submit"]'),
    page.waitForNavigation({ waitUntil: "networkidle2", timeout: 120000 }).catch(() => null),
  ]);

  // Fallback for SPAs without full navigation.
  await page.waitForFunction(
    () => window.location.pathname.startsWith("/dashboard"),
    { timeout: 30000 },
  );
}

async function captureScreens(page) {
  for (const item of routes) {
    const url = `${BASE_URL}${item.route}`;
    const out = path.join(OUTPUT_DIR, item.name);

    await page.goto(url, { waitUntil: "networkidle2", timeout: 120000 });
    await new Promise((resolve) => setTimeout(resolve, 1200));
    await page.screenshot({ path: out, type: "png", fullPage: false });
    console.log(`Saved: ${out}`);
  }
}

async function writeReadme() {
  const notePath = path.join(OUTPUT_DIR, "README.txt");
  const note = [
    "Authenticated iOS screenshots generated from production routes.",
    `Profile: ${selectedProfileKey}`,
    `Size: ${profile.screenshotPixels}`,
    `Base URL: ${BASE_URL}`,
    "Routes: /dashboard, /dashboard/conversations, /dashboard/contacts, /dashboard/crm, /dashboard/settings",
    "Generated by: scripts/ios-capture-authenticated-screenshots.mjs",
    "",
  ].join("\n");
  await fs.writeFile(notePath, note, "utf8");
}

async function main() {
  if (!DEMO_EMAIL || !DEMO_PASSWORD) {
    throw new Error(
      "Missing DEMO_EMAIL/DEMO_PASSWORD. Run with env vars, e.g. DEMO_EMAIL='demo@uzzai.com.br' DEMO_PASSWORD='***' pnpm run ios:screenshots:auth",
    );
  }

  await fs.mkdir(OUTPUT_DIR, { recursive: true });

  const browser = await puppeteer.launch({
    headless: true,
    defaultViewport: profile.viewport,
  });

  try {
    const page = await browser.newPage();
    await page.setUserAgent(profile.userAgent);
    await login(page);
    await captureScreens(page);
    await writeReadme();
  } finally {
    await browser.close();
  }
}

main().catch((error) => {
  console.error("Failed to generate authenticated screenshots:", error);
  process.exit(1);
});

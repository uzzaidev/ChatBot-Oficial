import fs from "node:fs/promises";
import path from "node:path";
import puppeteer from "puppeteer";

const BASE_URL = "https://uzzapp.uzzai.com.br";

const PROFILES = {
  "6_5": {
    outputDir: "docs/ios/screenshots/draft-6.5in",
    viewport: {
      width: 428,
      height: 926,
      deviceScaleFactor: 3,
      isMobile: true,
      hasTouch: true,
    },
    screenshotPixels: "1284x2778",
    label: "6.5-inch",
  },
  "6_7": {
    outputDir: "docs/ios/screenshots/draft-6.7in",
    viewport: {
      width: 430,
      height: 932,
      deviceScaleFactor: 3,
      isMobile: true,
      hasTouch: true,
    },
    screenshotPixels: "1290x2796",
    label: "6.7-inch",
    userAgent:
      "Mozilla/5.0 (iPhone; CPU iPhone OS 17_0 like Mac OS X) AppleWebKit/605.1.15 (KHTML, like Gecko) Version/17.0 Mobile/15E148 Safari/604.1",
  },
  ipad_13: {
    outputDir: "docs/ios/screenshots/draft-ipad-13in",
    viewport: {
      width: 1032,
      height: 1376,
      deviceScaleFactor: 2,
      isMobile: true,
      hasTouch: true,
    },
    screenshotPixels: "2064x2752",
    label: "13-inch iPad",
    userAgent:
      "Mozilla/5.0 (iPad; CPU OS 17_0 like Mac OS X) AppleWebKit/605.1.15 (KHTML, like Gecko) Version/17.0 Mobile/15E148 Safari/604.1",
  },
};

const selectedProfileKey = process.env.IOS_SCREENSHOT_PROFILE ?? "6_5";
const profile = PROFILES[selectedProfileKey] ?? PROFILES["6_5"];
const OUTPUT_DIR = path.resolve(profile.outputDir);

const pages = [
  { name: "01-login.png", route: "/login" },
  { name: "02-register.png", route: "/register" },
  { name: "03-privacy.png", route: "/privacy" },
  { name: "04-terms.png", route: "/terms" },
  { name: "05-support.png", route: "/support" },
];

async function main() {
  await fs.mkdir(OUTPUT_DIR, { recursive: true });

  const browser = await puppeteer.launch({
    headless: true,
    defaultViewport: profile.viewport,
  });

  try {
    const page = await browser.newPage();
    await page.setUserAgent(profile.userAgent ?? PROFILES["6_5"].userAgent);

    for (const item of pages) {
      const url = `${BASE_URL}${item.route}`;
      const filePath = path.join(OUTPUT_DIR, item.name);

      await page.goto(url, { waitUntil: "networkidle2", timeout: 120000 });
      await page.screenshot({
        path: filePath,
        type: "png",
        fullPage: false,
      });
      console.log(`Saved: ${filePath}`);
    }
  } finally {
    await browser.close();
  }

  const notePath = path.join(OUTPUT_DIR, "README.txt");
  const note = [
    "Draft iOS screenshots generated from production web routes.",
    `Profile: ${selectedProfileKey} (${profile.label})`,
    `Size: ${profile.screenshotPixels}.`,
    "To change profile, run with env IOS_SCREENSHOT_PROFILE=6_5, 6_7, or ipad_13.",
    "Generated by: scripts/ios-capture-draft-screenshots.mjs",
    "These are draft assets; final review may still require simulator/device captures.",
    "",
  ].join("\n");
  await fs.writeFile(notePath, note, "utf8");

  console.log(`Saved: ${notePath}`);
}

main().catch((error) => {
  console.error("Failed to capture draft screenshots:", error);
  process.exit(1);
});

  'üîò N√çVEL 1: Node Enabled (CR√çTICO!)' as categoria,
  config_key,
  config_value,
  config_value->>'enabled' as enabled_value,
  CASE
    WHEN config_value->>'enabled' = 'true' THEN '‚úÖ HABILITADO'
    WHEN config_value->>'enabled' = 'false' THEN '‚ùå DESABILITADO'
